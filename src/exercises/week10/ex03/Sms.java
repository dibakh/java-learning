package exercises.week10.ex03;

public class SmsSummarizer {
    private String title;
    private String text;

    public SmsSummarizer(String title, String text) {
        this.title = title;
        this.text = text;
    }

    public String getTitle() {
        return title;
    }

    public String getText() {
        return text;
    }
}
